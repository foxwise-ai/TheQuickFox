  @doc """
  Gets a device by UUID.

  ## Examples

      iex> get_device_by_uuid("some-uuid")
      %Device{}

      iex> get_device_by_uuid("nonexistent")
      nil

  """
  def get_device_by_uuid(uuid) when is_binary(uuid) do
    Repo.get_by(Device, device_uuid: uuid)
  end

  @doc """
  Gets a device by auth token.

  ## Examples

      iex> get_device_by_token("some-token")
      %Device{}

      iex> get_device_by_token("invalid")
      nil

  """
  def get_device_by_token(token) when is_binary(token) do
    Device
    |> Repo.get_by(auth_token: token)
    |> Repo.preload(:user)
  end

  @doc """
  Gets a user by Stripe customer ID.

  ## Examples

      iex> get_user_by_stripe_customer_id("cus_123")
      %User{}

      iex> get_user_by_stripe_customer_id("nonexistent")
      nil

  """
  def get_user_by_stripe_customer_id(stripe_customer_id) when is_binary(stripe_customer_id) do
    Repo.get_by(User, stripe_customer_id: stripe_customer_id)
  end
end
